package serial;

public class Serial {
	
	static public void print(String txt)
	{
		System.out.print(txt);
	}

	static public void println(String txt)
	{
		System.out.println(txt);
	}
	
	static public void print(int txt)
	{
		System.out.print(txt);
	}
	
	static public void println(int txt)
	{
		System.out.println(txt);
	}
	
}
